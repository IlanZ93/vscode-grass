export function getRandomMessage(lang: string, stage: GrassStage, touchCount: number): string {
  const l = (MESSAGES[lang as Lang] ? lang : 'en') as Lang;

  // Bug solved milestone every 10 touches
  if (touchCount > 0 && touchCount % 10 === 0) {
    return BUG_SOLVED[l].replace(/\{n\}/g, String(touchCount));
  }

  // Build a weighted pool: general + stage-specific + game refs all mixed equally
  const general = MESSAGES[l];
  const stagePool = STAGE_MESSAGES[stage]?.[l] ?? [];
  // Game refs get same weight per-entry as other messages
  const combined = [...general, ...stagePool, ...POPCULTURE_REFS_EN];
  return combined[Math.floor(Math.random() * combined.length)];
}

